package ar.edu.unq.poo2.tp2;

public class Concepto {
    private String nombre;
    private double monto;

    public Concepto(String nombre, double monto) {
        this.nombre = nombre;
        this.monto = monto;
    }

    public String getNombre() {
        return nombre;
    }

    public double getMonto() {
        return monto;
    }

}